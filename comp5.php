     <li> Recruitment & selection:<br>
      Recruiting candidates and selecting the best ones to come and work for the company 
         is a key HR responsibility. People are the lifeblood of the organization and 
         finding the best fits is a key task.</li>
      
      <li> Performance management:<br>
      Performance management is the second HR basic. It involves 
          helping people to become their best selves at work, boosting the company’s
          bottom line. </li>
      
      <li>Learning & development:<br>
      Learning and development helps employees to reskill and upskill. Learning &
          Development (L&D) is led by HR and good policies can be very helpful in 
          advancing the organization towards its long-term goals.</li>
      
      <li>Succession planning:<br>
      Succession planning is the process of planning contingencies in case of key employees
          leaving the company. If, for example, a crucial senior manager quits his/her 
          job, having a replacement ready will guarantee continuity and can save the
          company significant money.</li>
      
      <li>Compensation and benefits:<br>
      Compensation comprises primary compensation and secondary compensation.
          Primary compensation involves directly paid money for work, which often is
          a monthly salary and sometimes performance-based pay.
      </li>
      
      <li>Human Resources Information Systems:<br>
      The last two HR basics are not HR practices but tools to do HR better. The first 
          is the Human Resource Information System, or HRIS. An HRIS supports all the
          cornerstones we discussed above. For example, for recruitment and selection, 
          HR professionals often use an Applicant Tracking System, or ATS, to keep track 
          of applicants and hires.</li>
      
      <li>HR data and analytics:<br>
