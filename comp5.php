  <div class="dropdown-container" style="height:auto;">
      <p> The techniques and processes of data analytics have been automated into
          mechanical processes and algorithms that work over raw data for human c
          onsumption. Data analytics help a business optimize its performance.
          Analysis of data is a vital part of running a successful business. When data
          is used effectively, it leads to better understanding of a business’s previous perf
          ormance and better decision-making for its future activities. There are man
          y ways that data can be utilized, at all levels of a company’s operations<br>
      Types of Data Analytics:<br></p>
      
      <li>Descriptive Analysis:<br>
      Descriptive analysis answers the “what happened” by summarizing past data, usually in the form of dashboards.

The biggest use of descriptive analysis in business is to track Key Performance Indicators (KPIs). KPIs describe how a business is performing based on chosen benchmarks.</li>
      
      <li>Diagnostic Analysis: <br>
      Diagnostic analysis takes the insights found from descriptive analytics and drills down to find the causes of those outcomes. Organizations make use of this type of analytics as it creates more connections between data and identifies patterns of behavior.</li>
      
      <li>Predictive Analysis: <br>
      This type of analysis is another step up from the descriptive and diagnostic analyses. Predictive analysis uses the data we have summarized to make logical predictions of the outcomes of events. This analysis relies on statistical modeling, which requires added technology and manpower to forecast. It is also important to understand that forecasting is only an estimate; the accuracy of predictions relies on quality and detailed data.</li>
      
      <li>Prescriptive Analysis: <br>
       Prescriptive analysis is the frontier of data analysis, combining the insight from all previous analyses to determine the course of action to take in a current problem or decision.
<br>Prescriptive analysis utilizes state of the art technology and data practices. It is a huge organizational commitment and companies must be sure that they are ready and willing to put forth the effort and resources.
Artificial Intelligence (AI) is a perfect example of prescriptive analytics. AI systems consume a large amount of data to continuously learn and use this information to make informed decisions.</li>
      
      
      
      
